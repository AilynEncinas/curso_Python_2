print('Ingresa un numero')
num = int(input())
if(num<0):
    print('El numero '+str(num)+' es negativo');
if(num==0):
    print('El numero que ingresaste es 0');
if(num>0):
    print('El numero que ingresaste es positivo');
if(num>10 and num<20):
    print('El numero que ingresaste es mayor a 10 y menor a 20');
if(num>20):
    print('El numero que ingresaste es mayor a 20');